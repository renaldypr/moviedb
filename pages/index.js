import { useEffect, useState, useRef, useCallback } from 'react'
import { useDispatch, useSelector, shallowEqual } from 'react-redux'
import Head from 'next/head'
import Card from '../src/components/card'
import Footer from '../src/components/footer'
import styles from '../styles/Home.module.css'
import { fetchMovieList } from '../src/store/actions/movieAction'
import * as types from '../src/store/types'

export default function Home() {
  const [page, setPage] = useState(1)
  const [query, setQuery] = useState('')
  const [toggle, setToggle] = useState(false)
  const dispatch = useDispatch()
  const { movieList, isLoading, hasMore } = useSelector(
    (state) => state.movieReducer, shallowEqual
  )
  const observer = useRef()

  useEffect(() => {
    if (!query || !toggle) return
    dispatch(
      fetchMovieList({
        page: page,
        search: query
      })
    )
  }, [dispatch, page, query, toggle])

  const lastMovieElementRef = useCallback(
    (node) => {
      if (isLoading) return
      if (observer.current) observer.current.disconnect()
      observer.current = new IntersectionObserver((entries) => {
        if (entries[0].isIntersecting && hasMore) {
          setPage((prev) => prev + 1)
        }
      })
      if (node) observer.current.observe(node)
    },
    [isLoading, hasMore]
  )

  const handleChange = (e) => {
    setQuery(e.target.value)
    setToggle(false)
  }

  const handleSubmit = () => {
    setPage(1)
    setToggle(true)
    dispatch({
      type: types.CLEAR_MOVIE_LIST
    })
  }

  return (
    <div className={styles.container}>
      <Head>
        <title>Movie DB</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>Movie DB</h1>
        <p>Your #1 source for all movies!</p>

        <div className={styles.searchBarWrapper}>
          <input type="text" onChange={handleChange} value={query} placeholder="Type your movie title" />
          <button type="button" onClick={handleSubmit}>Search</button>
        </div>

        <div className={styles.grid}>
          {movieList.map((movie, i) => {
            const isLastElement = movieList.length === i + 1
            return (
              <Card
                movie={movie}
                key={i}
                ref={isLastElement ? lastMovieElementRef : null}
              />
            )
          })}
        </div>
        <div>{isLoading && "Loading..."}</div>
      </main>

      <Footer />
    </div>
  )
}
